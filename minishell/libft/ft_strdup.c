#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	char	*sdup;

	i = 0;
	sdup = (char *)malloc((sizeof(char) * ft_strlen(s)) + 1);
	if (sdup == NULL)
		return (NULL);
	while (s[i] != '\0')
	{
		sdup[i] = s[i];
		i++;
	}
	sdup[i] = '\0';
	return (sdup);
}
